function initParts() {
    const page = document.querySelector('.parts-page');
    if (!page) return;
    const tableWrap = document.getElementById('parts-table-wrap');
    const tocEl = document.getElementById('parts-toc');
    const searchEl = document.getElementById('parts-search');
    if (!tableWrap || !tocEl || !searchEl) return;

    // Clear TOC except the h1
    while (tocEl.children.length > 1) tocEl.removeChild(tocEl.lastChild);

    const sections = [];

    page.querySelectorAll('td.section-header').forEach((td, i) => {
        td.querySelectorAll('.parts-toggle').forEach(s => s.remove());
        const span = document.createElement('span');
        span.className = 'parts-toggle';
        td.prepend(span);

        const headerTr = td.closest('tr');
        headerTr.id = 'parts-section-' + i;

        const dataRows = [];
        let row = headerTr.nextElementSibling;
        while (row && !row.querySelector('td.section-header') && !row.classList.contains('cat-header-row')) {
            dataRows.push(row);
            row = row.nextElementSibling;
        }

        dataRows.forEach((r, j) => r.classList.toggle('alt', j % 2 === 1));

        td.setAttribute('role', 'button');
        td.setAttribute('tabindex', '0');
        td.setAttribute('aria-expanded', 'true');

        const sec = { td, span, headerTr, dataRows, open: true, label: td.textContent.trim(), tocLink: null };
        sec.setOpen = function(open) {
            sec.open = open;
            sec.span.textContent = open ? '\u25BC ' : '\u25B6 ';
            sec.td.setAttribute('aria-expanded', String(open));
            sec.dataRows.forEach(r => { r.style.display = open ? '' : 'none'; });
        };
        const toggle = () => sec.setOpen(!sec.open);
        td.addEventListener('click', toggle);
        td.addEventListener('keydown', e => {
            if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); toggle(); }
        });
        sections.push(sec);
    });

    // Remove old cat-header-rows before adding new ones
    page.querySelectorAll('tr.cat-header-row').forEach(r => r.remove());

    let lastCat = null;
    sections.forEach((s, i) => {
        const cat = PARTS_SECTION_CATEGORIES[i] || 'Other';
        if (cat !== lastCat) {
            const catRow = document.createElement('tr');
            catRow.className = 'cat-header-row';
            const catTd = document.createElement('td');
            catTd.colSpan = 4;
            catTd.textContent = cat;
            catRow.appendChild(catTd);
            s.headerTr.parentNode.insertBefore(catRow, s.headerTr);
            lastCat = cat;
        }
    });

    const seenCats = [];
    const byCat = {};
    sections.forEach((s, i) => {
        const cat = PARTS_SECTION_CATEGORIES[i] || 'Other';
        if (!byCat[cat]) { byCat[cat] = []; seenCats.push(cat); }
        byCat[cat].push({ s, i });
    });

    seenCats.forEach(cat => {
        const heading = document.createElement('div');
        heading.className = 'parts-toc-group';
        heading.textContent = cat;
        tocEl.appendChild(heading);
        byCat[cat].forEach(({ s, i }) => {
            const a = document.createElement('a');
            a.href = '#parts-section-' + i;
            a.title = s.label;
            const catMatch = s.label.match(/\s*[-\u2014]+\s*(powerhead|rail)(\s*\(([^)]+)\))?\s*$/i);
            const displayLabel = catMatch
                ? s.label.slice(0, s.label.length - catMatch[0].length).trim()
                : s.label.trim();
            a.appendChild(document.createTextNode(displayLabel));
            if (catMatch) {
                const tag = document.createElement('span');
                tag.className = 'parts-toc-tag';
                const base = /powerhead/i.test(catMatch[1]) ? 'head' : 'rail';
                const extra = catMatch[3] ? ': ' + catMatch[3].toLowerCase() : '';
                tag.textContent = base + extra;
                a.appendChild(tag);
            }
            const cnt = document.createElement('span');
            cnt.className = 'parts-toc-count';
            cnt.textContent = s.dataRows.length;
            a.appendChild(cnt);
            s.tocLink = a;
            tocEl.appendChild(a);
        });
    });

    tocEl.querySelectorAll('a').forEach(a => {
        a.addEventListener('click', e => {
            e.preventDefault();
            const target = document.getElementById(a.getAttribute('href').slice(1));
            if (!target || !tableWrap) return;
            const top = target.getBoundingClientRect().top - tableWrap.getBoundingClientRect().top + tableWrap.scrollTop - 2;
            tableWrap.scrollTo({ top, behavior: 'smooth' });
        });
    });

    const origHTML = new Map();
    sections.forEach(s => s.dataRows.forEach(r => {
        origHTML.set(r, Array.from(r.querySelectorAll('td')).map(c => c.innerHTML));
    }));

    function escRe(s) { return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }

    searchEl.addEventListener('input', function() {
        const q = this.value.trim();
        const ql = q.toLowerCase();
        sections.forEach(s => {
            s.dataRows.forEach(r => {
                const orig = origHTML.get(r);
                r.querySelectorAll('td').forEach((c, j) => { c.innerHTML = orig[j]; });
            });
            if (!q) {
                s.headerTr.style.display = '';
                s.dataRows.forEach(r => { r.style.display = s.open ? '' : 'none'; });
                return;
            }
            const re = new RegExp(escRe(q), 'gi');
            let hasMatch = false;
            s.dataRows.forEach(r => {
                const match = r.textContent.toLowerCase().includes(ql);
                r.style.display = match ? '' : 'none';
                if (match) {
                    hasMatch = true;
                    r.querySelectorAll('td').forEach(c => {
                        c.innerHTML = c.innerHTML.replace(re, '<mark>$&</mark>');
                    });
                }
            });
            s.headerTr.style.display = hasMatch ? '' : 'none';
        });
    });

    sections.forEach(s => s.dataRows.forEach(r => {
        const firstTd = r.querySelector('td:first-child');
        if (!firstTd) return;
        const pn = firstTd.childNodes[0] ? firstTd.childNodes[0].textContent.trim() : '';
        if (!pn) return;
        const btn = document.createElement('button');
        btn.className = 'parts-copy-btn';
        btn.title = 'Copy part number';
        btn.textContent = '\u2398';
        btn.addEventListener('click', e => {
            e.stopPropagation();
            navigator.clipboard.writeText(pn).then(() => {
                btn.textContent = '\u2713';
                btn.style.color = 'var(--accent)';
                setTimeout(() => { btn.textContent = '\u2398'; btn.style.color = ''; }, 1400);
            });
        });
        firstTd.appendChild(btn);
    }));

    sections.forEach(s => s.setOpen(true));

    const spy = new IntersectionObserver(entries => {
        entries.forEach(entry => {
            if (!entry.isIntersecting) return;
            const s = sections.find(sec => sec.headerTr === entry.target);
            if (!s || !s.tocLink) return;
            tocEl.querySelectorAll('a.active').forEach(a => a.classList.remove('active'));
            s.tocLink.classList.add('active');
            const linkTop = s.tocLink.offsetTop - tocEl.offsetTop;
            const linkBottom = linkTop + s.tocLink.offsetHeight;
            if (linkTop < tocEl.scrollTop) {
                tocEl.scrollTop = linkTop - 4;
            } else if (linkBottom > tocEl.scrollTop + tocEl.clientHeight) {
                tocEl.scrollTop = linkBottom - tocEl.clientHeight + 4;
            }
        });
    }, { root: tableWrap, rootMargin: '-2px 0px -80% 0px', threshold: 0 });
    sections.forEach(s => spy.observe(s.headerTr));

    const diagOverlay = document.getElementById('parts-diag-overlay');
    if (diagOverlay) {
        const closeBtn = document.getElementById('parts-diag-close');
        if (closeBtn) closeBtn.addEventListener('click', partsCloseDiagram);
        diagOverlay.addEventListener('click', e => { if (e.target === diagOverlay) partsCloseDiagram(); });
    }

    window._partsSearchEl = searchEl;
}

function partsExpandAll() {
    if (!document.querySelector('.parts-page')) return;
    document.querySelectorAll('.parts-page td.section-header').forEach(td => {
        const tr = td.closest('tr');
        let r = tr.nextElementSibling;
        while (r && !r.querySelector('td.section-header') && !r.classList.contains('cat-header-row')) {
            r.style.display = '';
            r = r.nextElementSibling;
        }
        const span = td.querySelector('.parts-toggle');
        if (span) span.textContent = '\u25BC ';
        td.setAttribute('aria-expanded', 'true');
    });
}

function partsCollapseAll() {
    if (!document.querySelector('.parts-page')) return;
    document.querySelectorAll('.parts-page td.section-header').forEach(td => {
        const tr = td.closest('tr');
        let r = tr.nextElementSibling;
        while (r && !r.querySelector('td.section-header') && !r.classList.contains('cat-header-row')) {
            r.style.display = 'none';
            r = r.nextElementSibling;
        }
        const span = td.querySelector('.parts-toggle');
        if (span) span.textContent = '\u25B6 ';
        td.setAttribute('aria-expanded', 'false');
    });
}

function partsOpenDiagram(src, title) {
    const overlay = document.getElementById('parts-diag-overlay');
    const img = document.getElementById('parts-diag-img');
    const titleEl = document.getElementById('parts-diag-title');
    if (!overlay) return;
    img.src = src;
    titleEl.textContent = title;
    overlay.classList.add('open');
}

function partsCloseDiagram() {
    const overlay = document.getElementById('parts-diag-overlay');
    if (!overlay) return;
    overlay.classList.remove('open');
    setTimeout(() => {
        const img = document.getElementById('parts-diag-img');
        if (img) img.src = '';
    }, 200);
}

document.addEventListener('keydown', e => {
    const el = window._partsSearchEl;
    if (!el || !document.querySelector('.parts-page')) return;
    if (e.key === '/' && document.activeElement !== el) {
        e.preventDefault();
        el.focus();
        el.select();
    } else if (e.key === 'Escape' && document.activeElement === el) {
        el.blur();
    }
});
